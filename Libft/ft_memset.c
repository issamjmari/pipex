#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*t;

	i = 0;
	t = (char *) b;
	while (i < len)
	{
		t[i] = c;
		i++;
	}
	return (t);
}
